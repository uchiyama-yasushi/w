#include <sys/types.h>
#include <reg.h>
#include <libaki/aki_switch.h>

void
aki_switch_init ()
{
  // DIP Switch
  *P2_DDR = 0x00;	// IN
  *P2_PCR = 0xff;	// Pullup

  // Tactile Switch
  *P4_DDR = 0x00;	// IN
  *P4_PCR = 0xff;	// Pullup
}
